import 'package:flutter/material.dart';

class StylesApp {
    static TextStyle textCommonBold(Color color) => TextStyle(fontSize: 14, fontWeight: FontWeight.bold, color: color);

    static TextStyle textCommonNormal(Color color) => TextStyle(fontSize: 14, fontWeight: FontWeight.normal, color: color);

    static TextStyle textMiddleBold(Color color) => TextStyle(fontSize: 18, fontWeight: FontWeight.bold, color: color);

    static TextStyle textMiddleNormal(Color color) => TextStyle(fontSize: 18, fontWeight: FontWeight.normal, color: color);
}
